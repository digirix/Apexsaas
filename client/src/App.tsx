import { Switch, Route } from "wouter";
import { queryClient } from "@/lib/queryClient";
import { QueryClientProvider } from "@tanstack/react-query";
import { Toaster } from "@/components/ui/toaster";
import { AuthProvider } from "@/hooks/use-auth";
import { ProtectedRoute } from "@/lib/protected-route";
import { ChatWidget } from "@/components/chatbot/chat-widget";
import NotFound from "@/pages/not-found";
import AuthPage from "@/pages/auth-page";
import DashboardPage from "@/pages/dashboard-page";
import ClientsPage from "@/pages/clients-page";
import ClientDetailPage from "@/pages/client-detail-page";
import SetupPage from "@/pages/setup-page";
import TasksPage from "@/pages/tasks-page";
import UsersPage from "@/pages/users-page";
import SettingsPage from "@/pages/settings-page";
import { ComplianceCalendarPage } from "@/pages/compliance-calendar-page";
import AutoGeneratedTasksPage from "@/pages/auto-generated-tasks-page";
import AiReportingPage from "@/pages/ai-reporting-page";
import FinancePage from "@/pages/finance";
import CreateInvoicePage from "@/pages/finance/invoices/create";
import CreateInvoiceFromTaskPage from "@/pages/finance/invoices/from-task";
import CreatePaymentPage from "@/pages/finance/payments/create";
import CreateChartOfAccountPage from "@/pages/finance/chart-of-accounts/create";
import CreateJournalEntryPage from "@/pages/finance/journal-entries/create";
import EditJournalEntryPage from "@/pages/finance/journal-entries/edit";
import ViewJournalEntryPage from "@/pages/finance/journal-entries/view";
import AccountHeadsManagementPage from "@/pages/finance/account-heads";
import COAConfigurationPage from "@/pages/setup/coa-configuration";
import AiCustomizationPage from "@/pages/setup/ai-customization";
import FinancialReportsPage from "@/pages/finance/reports";
import LedgerReportPage from "@/pages/finance/ledger";
import ProfitAndLossPage from "@/pages/finance/reports/profit-loss";
import BalanceSheetPage from "@/pages/finance/reports/balance-sheet";
import CashFlowPage from "@/pages/finance/reports/cash-flow";
import ExpenseReportPage from "@/pages/finance/reports/expenses";
import TaxSummaryPage from "@/pages/finance/reports/tax-summary";

function Router() {
  return (
    <Switch>
      <ProtectedRoute path="/" component={DashboardPage} />
      <ProtectedRoute path="/clients" component={ClientsPage} />
      <ProtectedRoute path="/clients/:id" component={ClientDetailPage} />
      <ProtectedRoute path="/setup" component={SetupPage} />
      <ProtectedRoute path="/setup/coa-configuration" component={COAConfigurationPage} />
      <ProtectedRoute path="/setup/ai-customization" component={AiCustomizationPage} />
      <ProtectedRoute path="/tasks" component={TasksPage} />
      <ProtectedRoute path="/auto-generated-tasks" component={AutoGeneratedTasksPage} />
      <ProtectedRoute path="/compliance-calendar" component={ComplianceCalendarPage} />
      <ProtectedRoute path="/ai-reporting" component={AiReportingPage} />
      <ProtectedRoute path="/finance" component={FinancePage} />
      {/* Direct invoice creation disabled - invoices are now created only through tasks */}
      {/* <ProtectedRoute path="/finance/invoices/create" component={CreateInvoicePage} /> */}
      <ProtectedRoute path="/finance/invoices/from-task" component={CreateInvoiceFromTaskPage} />
      <ProtectedRoute path="/finance/payments/create" component={CreatePaymentPage} />
      <ProtectedRoute path="/finance/account-heads" component={AccountHeadsManagementPage} />
      <ProtectedRoute path="/finance/chart-of-accounts/create" component={CreateChartOfAccountPage} />
      <ProtectedRoute path="/finance/journal-entries/create" component={CreateJournalEntryPage} />
      <ProtectedRoute path="/finance/journal-entries/edit/:id" component={EditJournalEntryPage} />
      <ProtectedRoute path="/finance/journal-entries/view/:id" component={ViewJournalEntryPage} />
      <ProtectedRoute path="/finance/reports" component={FinancialReportsPage} />
      <ProtectedRoute path="/finance/ledger" component={LedgerReportPage} />
      <ProtectedRoute path="/finance/reports/profit-loss" component={ProfitAndLossPage} />
      <ProtectedRoute path="/finance/reports/balance-sheet" component={BalanceSheetPage} />
      <ProtectedRoute path="/finance/reports/cash-flow" component={CashFlowPage} />
      <ProtectedRoute path="/finance/reports/expenses" component={ExpenseReportPage} />
      <ProtectedRoute path="/finance/reports/tax-summary" component={TaxSummaryPage} />
      <ProtectedRoute path="/users" component={UsersPage} />
      <ProtectedRoute path="/settings" component={SettingsPage} />
      <Route path="/auth" component={AuthPage} />
      <Route component={NotFound} />
    </Switch>
  );
}

function App() {
  return (
    <QueryClientProvider client={queryClient}>
      <AuthProvider>
        <Router />
        <ChatWidget />
        <Toaster />
      </AuthProvider>
    </QueryClientProvider>
  );
}

export default App;
